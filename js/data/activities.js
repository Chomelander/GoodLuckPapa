const DOMAIN_MAP = {
  L: 'language',
  S: 'sensorial',
  M: 'movement',
  P: 'practical_life',
  E: 'emotional_social',
};

// 典型专注时长（秒），与 rules.js TYPICAL_SEC 保持一致
const TYPICAL_SEC_TABLE = {
  language:         { '0-3': 45, '3-6': 60, '6-12': 90 },
  sensorial:        { '0-3': 60, '3-6': 90, '6-12': 120 },
  movement:         { '0-3': 30, '3-6': 60, '6-12': 90 },
  emotional_social: { '0-3': 30, '3-6': 45, '6-12': 60 },
  practical_life:   { '0-3': 45, '3-6': 60, '6-12': 90 },
};

function typicalSec(rawDomain, minAge) {
  const domain = DOMAIN_MAP[rawDomain] ?? 'language';
  const band = minAge < 3 ? '0-3' : minAge < 6 ? '3-6' : '6-12';
  return TYPICAL_SEC_TABLE[domain]?.[band] ?? 60;
}

function normalize(raw) {
  return {
    ...raw,
    title: raw.name,
    domain: DOMAIN_MAP[raw.domain] ?? raw.domain,
    ageRange: [raw.minAge, raw.maxAge],
    typicalSec: typicalSec(raw.domain, raw.minAge),
    // observeFocus: [{look, why}] → string[]
    observeFocus: (raw.observeFocus ?? []).map(f =>
      typeof f === 'string' ? f : f.look
    ),
    // phases: tip → desc
    phases: (raw.phases ?? []).map(({ label, tip }) => ({ label, desc: tip ?? '' })),
  };
}

